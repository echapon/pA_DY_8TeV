void filterNegatives(TH2D* h) {
  for (int i = 1; i < h->GetXaxis()->GetNbins(); i++) {
    for (int j = 1; j < h->GetYaxis()->GetNbins(); j++) {
      if (h->GetBinContent(i+1,j+1) < 0) 
        h->SetBinContent(i+1,j+1,(h->GetBinContent(i+1,j)+h->GetBinContent(i+1,j+2))/2.);  
    }
  } 
}

